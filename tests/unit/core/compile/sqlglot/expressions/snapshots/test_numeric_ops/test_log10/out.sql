WITH `bfcte_0` AS (
  SELECT
    `float64_col`
  FROM `bigframes-dev`.`sqlglot_test`.`scalar_types`
)
SELECT
  CASE
    WHEN `float64_col` IS NULL
    THEN NULL
    WHEN `float64_col` > 0
    THEN LOG(`float64_col`, 10)
    WHEN `float64_col` < 0
    THEN CAST('NaN' AS FLOAT64)
    ELSE CAST('-Infinity' AS FLOAT64)
  END AS `float64_col`
FROM `bfcte_0`