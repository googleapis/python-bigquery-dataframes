WITH `bfcte_0` AS (
  SELECT
    `bool_col`,
    `int64_col`,
    `rowindex`
  FROM `bigframes-dev`.`sqlglot_test`.`scalar_types`
)
SELECT
  `rowindex`,
  `int64_col`,
  `bool_col`,
  `int64_col` < `int64_col` AS `int_lt_int`,
  `int64_col` < 1 AS `int_lt_1`,
  `int64_col` < CAST(`bool_col` AS INT64) AS `int_lt_bool`,
  CAST(`bool_col` AS INT64) < `int64_col` AS `bool_lt_int`
FROM `bfcte_0`