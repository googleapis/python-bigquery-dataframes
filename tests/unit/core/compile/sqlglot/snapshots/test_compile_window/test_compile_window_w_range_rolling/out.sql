), `bfcte_1` AS (
  SELECT
    *,
    CASE
      WHEN COALESCE(
        SUM(CAST(NOT `bfcol_1` IS NULL AS INT64)) OVER (
          ORDER BY UNIX_MICROS(`bfcol_0`) ASC
          RANGE BETWEEN 2999999 PRECEDING AND CURRENT ROW
        ),
        0
      ) < 1
      THEN NULL
      ELSE COALESCE(
        SUM(`bfcol_1`) OVER (
          ORDER BY UNIX_MICROS(`bfcol_0`) ASC
          RANGE BETWEEN 2999999 PRECEDING AND CURRENT ROW
        ),
        0
      )
    END AS `bfcol_6`
  FROM `bfcte_0`
)
SELECT
  `bfcol_0` AS `ts_col`,
  `bfcol_6` AS `int_col`
FROM `bfcte_1`
